        const megaMenu = document.querySelector('#megaDropdown + .mega-menu');
        if (!megaMenu) return;

        // Get all columns inside the mega menu
        const cols = megaMenu.querySelectorAll('.mega-col');

        // Intersection Observer for smooth reveal on scroll
        const observer = new IntersectionObserver(
            (entries) => {
                entries.forEach((entry) => {
                    if (entry.isIntersecting) {
                        entry.target.classList.add('visible');
                    }
                });
            },
            {
                threshold: 0.2,
            }
        );

        cols.forEach((col) => observer.observe(col));
    });
